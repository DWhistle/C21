#include "libft.h"

void	ft_bzero(void *d, size_t l)
{
	char	*dest;

	if (l == 0)
		return ;
	dest = (char *)d;
	while (l > 0)
	{
		*dest = 0;
		dest++;
		l--;
	}
}
