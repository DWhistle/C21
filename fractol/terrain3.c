#include "fractal.h"

void	ter_clean(t_ter **e, int scale)
{
	int i;

	i = 0;
	while (i < scale)
	{
		e[0][i].z = 0;
		e[scale - 1][i].z = 0;
		if (i > 0)
		{
			e[i - 1][0].z = 0;
			e[i - 1][scale - 1].z = 0;
		}
		i++;
	}
}
